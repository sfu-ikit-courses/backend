def get_plural_form(number: int, *, one: str, few: str, many: str) -> str:
    """
    Возвращает правильную форму слова в зависимости от числа по правилам русского языка.

    Args:
        number (int): Число, с которым связано слово (например, количество объектов).
        one (str): Форма слова, используемая с числом, оканчивающимся на 1 (кроме 11), например: "статья".
        few (str): Форма слова, используемая с числами, оканчивающимися на 2, 3, 4 (кроме 12, 13, 14), например: "статьи".
        many (str): Форма слова, используемая со всеми остальными числами, например: "статей".

    Returns:
        str: Корректная форма слова, соответствующая переданному числу.

    Example:
        ```python
        form = get_plural_form(3, one="статья", few="статьи", many="статей")
        print(form)  # Вывод: "статьи"
        ```
    """
    number = abs(number)
    if number % 10 == 1:
        return one
    elif number % 10 in [2, 3, 4]:
        return few
    else:
        return many
